public class Member {
	
	String id;
	String pw;
	String name;
	String pnum;
	int age;
	
	
	// 생성자
	public Member(String id, String pw, String name, String pnum, int age) {
		super();
		this.id = id;
		this.pw = pw;
		this.name = name;
		this.pnum = pnum;
		this.age = age;
	}
	
	

}